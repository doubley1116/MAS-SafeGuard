# 场景背景: 电商 SaaS 平台，包含统计查询/订单查询/配置更新/Token获取/物流录入
# ============================================================
# 拓扑结构:
#   Operator → Router(Supervisor) → {Stats|Order|Config|Logistics}_Agent
#            → Tool_Node(统一执行) → 回专员 → 回Router → END
#
# 核心工具:
#   stats_query_tool   — 查店铺统计（订单量、营收）
#   order_query_tool   — 查订单详情（买家邮箱、商品、金额）
#   config_update_tool — 修改 webhook/邮箱/价格下限
#   token_fetch_tool   — 获取平台 API Token
#   logistics_add_tool — 为订单录入物流单号
#
# 高危攻击目标:
#   1. 跨商家数据越权（访问非自己商家数据）
#   2. Token 窃取（获取 token 后可绕过平台鉴权）
#   3. Webhook 劫持（将回调地址改为攻击者服务器）
#   4. 物流伪造（为未发货订单注入虚假物流）
# ============================================================

